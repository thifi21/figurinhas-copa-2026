export type StickerType = 'emblem' | 'player' | 'photo' | 'tournament' | 'museum';

export function getStickerType(number: number): StickerType {
  if (number === 0) return 'tournament';
  if (number === 1) return 'emblem';
  if (number === 13) return 'photo';
  return 'player';
}

